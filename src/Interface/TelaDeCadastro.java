    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        
        try {
            Filmes obj = new Filmes();

            if(Netflix.isSelected()){
                obj.setAssiste(Netflix.getText());
            }

            if(Mega.isSelected()){
                obj.setAssiste(Mega.getText());
            }

            if(Amazon.isSelected()){
                obj.setAssiste(Amazon.getText());
            }

            if(Downloads.isSelected()){
                obj.setAssiste(Downloads.getText());
            }

            

            obj.setNome(txtNome.getText());

            

            JOptionPane.showMessageDialog(null,"Cadastrado com sucesso");

            FilmesDAO dao = new FilmesDAO();
            dao.cadastrarFilme(obj);
            //-------------------------------
        } catch (Exception erro) {

            JOptionPane.showMessageDialog(null,"Erro" + erro);
        }
        
        
        
        
       
        
   
     
        
        

        

        
        
        
        
    }//GEN-LAST:event_btnAdicionarActionPerformed


    public void Listar(){
        try {
            //Passo 1 - executar o select
            FilmesDAO dao = new FilmesDAO();
            List<Filmes> listadefilmes = dao.ListarFilmes();
            
            //Passo 2 - colocar os dados na tabela
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            modelo.setNumRows(0);
            
            for (Filmes c : listadefilmes){
                modelo.addRow (new Object[]{
                c.getCodfilme(),
                c.getNome(),
                c.getAssiste(),       
                });
            
            }
        } catch (Exception e) {
        }
    }
   
